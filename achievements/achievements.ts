export type Difficulty = 'baby' | 'easy' | 'medium' | 'hard' | 'professional';

export interface Achievement {
	id: string,
	difficulty: Difficulty,
	title: string,
	condition: string,
	category: string,
	counter?: string,
	reaction?: string,
	value?: number,
	manual?: true,
}

const reactionAchievementFactory = (title: string, reaction: string, value: number): Achievement => {
	let difficulty: Difficulty = 'medium';
	if (value <= 3) {
		difficulty = 'medium';
	} else if (value <= 5) {
		difficulty = 'hard';
	} else if (value <= 10) {
		difficulty = 'professional';
	}
	return {
		id: `reaction-${reaction}-${value}`,
		difficulty,
		title,
		condition: `1つのメッセージに対し${value}人以上から:${reaction}:リアクションをもらう`,
		category: 'reactions',
		reaction,
		value,
	};
};

